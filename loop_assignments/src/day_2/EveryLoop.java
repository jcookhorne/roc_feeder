package day_2;

public class EveryLoop {
	
	public static void main(String[] args) {
// for , while, do while
	for (int i=25; i>=0; i-- ) {
		System.out.println("i = " +i);
	}
	int x =15;
	int y =1;
	
	while (y!=x) {
		System.out.println(++y);
	}

	String t= "Hello";
	int v=0;
	do {
		System.out.println(t);
	}
	while (v!=0); {
		
	}
	
	
	
	
	
	}
}
